package com.datastructure.linkedlist;

public class SingleLinkedListNode {

    private Integer data;
    private SingleLinkedListNode nextRef;

    public Integer getData() {
        return data;
    }

    public void setData(Integer data) {
        this.data = data;
    }

    public SingleLinkedListNode getNextRef() {
        return nextRef;
    }

    public void setNextRef(SingleLinkedListNode nextRef) {
        this.nextRef = nextRef;
    }
}
